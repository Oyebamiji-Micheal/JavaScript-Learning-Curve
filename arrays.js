// Arrays

// Defining an array
const courses = ['Algebra', 'Mechanics', 'Statistics', 'Calulus']
console.log(courses)

// Common array methods

// Adding an element to the end of the array
courses.push('Geometry')
console.log(courses)

// Array Slicing
console.log(courses.slice(2, ))

// Getting the index of an element in the array
console.log(courses.indexOf('Mechanics'))

// Number of elements in an array
console.log(`There are ${courses.length} elements in the array`)

